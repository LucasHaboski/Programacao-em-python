num1 = int(input('Digite um numero: '))
num2 = int(input('Digite outro numero: '))

print('\n{} + {} = {}'.format(num1, num2, num1 + num2))  # Soma
print('\n{} - {} = {}'.format(num1, num2, num1 - num2))  # Subtração
print('\n{} * {} = {}'.format(num1, num2, num1 * num2))  # Multiplicação
print('\n{} / {} = {}'.format(num1, num2, num1 / num2))  # Divisão
print('\n{} ^ {} = {}'.format(num1, num2, num1 ** num2)) # Potenciação
print('\n{} // {} = {}'.format(num1, num2, num1 // num2))# Divisão inteira
print('\n{} % {} = {}'.format(num1, num2, num1 % num2))  # Resto do divisão
